import { inngest } from "@/inngest/client";
import prisma from "@/lib/prisma";

// D√©clare la fonction en 3 arguments s√©par√©s
export const logBrandArchived = inngest.createFunction(
  {
    id: "log-brand-archived",
    name: "Log activit√© - Marque archiv√©e",
  },
  {
    event: "activity/brand.archived",
  },
  async ({ event }) => {
    const {
      action,
      entityType,
      entityId,
      oldData,
      newData,
      userId,
      organisationId,
    } = event.data;

    // R√©cup√©rer l'adresse IP via l'API ipify
    const response = await fetch("https://api.ipify.org?format=json");
    const data = await response.json();
    const ip = data.ip; // L'adresse IP r√©cup√©r√©e

    // Enregistrer l'action dans le log d'activit√© avec l'adresse IP
    await prisma.activityLog.create({
      data: {
        action,
        entityType,
        entityId,
        oldData: JSON.stringify(oldData),
        newData: JSON.stringify(newData),
        userId,
        organisationId,
        ipAddress: ip, // Ajout de l'adresse IP dans le log
      },
    });

    console.log(`üìù Log archiv√© pour la marque : ${entityId}, IP : ${ip}`);
  }
);
